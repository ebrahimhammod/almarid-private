                        return;
                    }

                    // التحقق من البريد الإلكتروني
                    const emailPattern = /^[^\s@]+@([^\s@]+\.)+[^\s@]+$/;
                    if (!emailPattern.test(email)) {
                        alert('الرجاء إدخال بريد إلكتروني صحيح');
                        return;
